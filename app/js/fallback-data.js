function fallbackRand(items) {
  return items[Math.floor(Math.random() * items.length)];
}

/* 智能话术匹配：根据用户输入的关键词返回最合适的回应 */
function smartFallback(input) {
  if (!input || typeof input !== 'string') {
    return fallbackRand(RESPONSE_POOLS._default);
  }
  const text = input.trim().toLowerCase();
  // 按优先级依次匹配关键词组
  for (const [key, pool] of Object.entries(RESPONSE_POOLS)) {
    if (key === '_default') continue;
    const keywords = key.split('|');
    for (const kw of keywords) {
      if (text.includes(kw.trim().toLowerCase())) {
        return fallbackRand(pool);
      }
    }
  }
  // 没有匹配到任何关键词 → 兜底
  return fallbackRand(RESPONSE_POOLS._default);
}


